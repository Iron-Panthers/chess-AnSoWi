public class Piece {
// Parent class for each piece
    public int x,y;
    public boolean isWhite;
    public String name;


    public Piece(int x, int y, Boolean isWhite, String name)
    {
        this.x = x;
        this.y = y;
        this.isWhite = isWhite;
        this.name = name;
    }

    


}